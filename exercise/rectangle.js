// find area rectangle
// rumus menemukan luas persegi panjang adalah ~ panjang * lebar 
// membuat 2 variable untuk menampung panjang & lebar
let panjang = 10
let lebar = 15
// mengalikan kedua variabel panjang dan lebar
let luas = panjang * lebar
// menampilkan hasil perkalian
console.log(`luas persegi panjang dengan panjang ${panjang} dan lebar ${lebar} adalah ${luas}`)


// find perimeter rectangle
// rumus menemukan keliling persegi panjang adalah ~ 2 * (p + l)
let keliling = 2 * (panjang + lebar)
// menampilkan hasil kelilingnya 
console.log(`keliling persegi panjang dengan panjang ${panjang} dan lebar ${lebar} adalah ${keliling}`)