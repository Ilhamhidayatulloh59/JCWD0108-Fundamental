// find diamter, circumference & area of circle
let jari = 7
const phi = 3.14

// rumus menemukan diameter ~ 2 * jari
let diameter = 2 * jari
// rumus menemukan keliling ~ diameter * phi
let keliling = diameter * phi
// rumus menemukan luas ~ phi * jari^2
let luas = phi * jari ** 2

// menampilkan hasil
console.log(`diameter, keliling $ luas lingkaran dengan jari jari ${jari} adalah ${diameter}, ${keliling} ${luas}`);